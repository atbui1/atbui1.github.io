function myfind(masp) {
    var c = window.localStorage.getItem("count");
    if (c != null) {
        for (var i = 0; i < c; i++) {
            var s = window.localStorage.getItem(i);
            if (s != null) {
                if (s.match(masp) != null)
                    return i;
            }
        }
    }
    return -1;
}
