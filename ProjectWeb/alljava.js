function showgiohang() {

    var sum = 0;

    var tmp = "<table>";
    tmp = tmp + "<caption>Thông tin giỏ hàng</caption>";
    tmp = tmp + "<tr>";
    tmp = tmp + "<th>Tên sản phẩm</th>";
    tmp = tmp + "<th>Số lượng</th>";
    tmp = tmp + "<th>Giá </th>";
    tmp = tmp + "</tr>";

    var c = window.localStorage.getItem("count");
    if (c != null) {
        for (var i = 0; i < c; i++) {
            var s = window.localStorage.getItem(i);
            if (s != null) {
                var arr = s.split(",");
                tmp = tmp + "<tr>";

                tmp = tmp + "<td>" + arr[1] + "<br \> <img src='" + arr[2] + "'></td>";

                tmp = tmp + "<td><input type='number' value='" + arr[4] + "' min='1' max='10' step='1' onchange='update(" + i + ",this)'></td>";
                tmp = tmp + "<td>" + arr[3] + "</td>";
                tmp = tmp + "<td><input type='button' value='X' onclick='xoa(" + i + ")' </td>"
                tmp = tmp + "</tr>";

                //tinh ong tien
                if(arr[4]>=0){
                if((arr[4]*10)%10 == 0){
                sum = sum + arr[3] * arr[4];
                }else{
                    window.alert("vui lòng không nhập số lẻ. Xin vui lòng nhập lại");
                }
                }else{
                    window.alert("vui lòng không nhập số âm( nhỏ hơn 0). Xin vui lòng nhập lại");
                }
            }
        }
    }
    tmp = tmp + "</table>";
    tmp = tmp + "<p align='center'>Tong tien: " + sum + " VNĐ</p>"
    var div = document.getElementById("giohang");
    div.innerHTML = tmp;
}
